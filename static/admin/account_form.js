document.addEventListener("DOMContentLoaded", function () {

    // --------------------------------
    // 3. Auto-suggest code from name
    // --------------------------------
    const nameField = document.getElementById("id_name");
    const codeField = document.getElementById("id_code");

    if (nameField && codeField) {
        nameField.addEventListener("input", function () {
            if (codeField.value === "" || codeField.dataset.autoGenerated === "true") {
                const words = nameField.value.trim().split(/\s+/);
                const initials = words.map(w => w[0]).join("").toUpperCase().substring(0, 4);
                const random = Math.random().toString(36).substring(2, 5).toUpperCase();
                codeField.value = initials ? `PH-${initials}-${random}` : "";
                codeField.dataset.autoGenerated = "true";
            }
        });

        // if user manually types in code, stop auto-generating
        codeField.addEventListener("input", function () {
            codeField.dataset.autoGenerated = "false";
        });
    }


    // --------------------------------
    // 2. Live character counter
    // --------------------------------
    const fieldsWithCounter = [
        { id: "id_location", max: 500 },
        { id: "id_designation", max: 500 },
    ];

    fieldsWithCounter.forEach(({ id, max }) => {
        const field = document.getElementById(id);
        if (!field) return;

        const counter = document.createElement("div");
        counter.style.cssText = "font-size: 0.78rem; margin-top: 4px; color: #888;";
        counter.textContent = `0 / ${max} characters`;
        field.parentNode.insertBefore(counter, field.nextSibling);

        field.addEventListener("input", function () {
            const len = field.value.length;
            counter.textContent = `${len} / ${max} characters`;
            counter.style.color = len > max * 0.9 ? "#e74c3c" : "#888";
        });
    });


    // --------------------------------
    // 4. Green/red validation feedback
    // --------------------------------
    const validatedFields = [
        { id: "id_code",     validate: v => v.length >= 3 },
        { id: "id_name",     validate: v => v.length >= 3 },
        { id: "id_city",     validate: v => v.length >= 2 },
        {id: "id_phone",     validate: v => /^(0[5-7]\d{8})$/.test(v)},
        { id: "id_email",    validate: v => /^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(v) },
        { id: "id_location", validate: v => v.length >= 5 },
    ];

    validatedFields.forEach(({ id, validate }) => {
        const field = document.getElementById(id);
        if (!field) return;

        const feedback = document.createElement("div");
        feedback.style.cssText = "font-size: 0.78rem; margin-top: 4px; font-weight: 500;";
        field.parentNode.insertBefore(feedback, field.nextSibling);

        field.addEventListener("input", function () {
            const valid = validate(field.value.trim());
            field.style.borderColor = valid ? "#27ae60" : "#e74c3c";
            feedback.textContent = valid ? "✓ Looks good" : "✗ Please check this field";
            feedback.style.color = valid ? "#27ae60" : "#e74c3c";
        });

        field.addEventListener("blur", function () {
            if (field.value.trim() === "") {
                field.style.borderColor = "";
                feedback.textContent = "";
            }
        });
    });


    // --------------------------------
    // 5. Auto-fill city from a list
    // --------------------------------
    const moroccanCities = [
        "Casablanca", "Rabat", "Fès", "Marrakech", "Tanger", "Meknès",
        "Oujda", "Kenitra", "Agadir", "Tétouan", "Safi", "El Jadida",
        "Nador", "Béni Mellal", "Errachidia", "Taza", "Settat", "Larache",
        "Khouribga", "Guelmim", "Berrechid", "Khémisset", "Taourirt",
        "Dakhla", "Laâyoune", "Ifrane", "Azrou", "Mohammedia"
    ];

    const cityField = document.getElementById("id_city");
    if (cityField) {
        // create datalist
        const datalist = document.createElement("datalist");
        datalist.id = "city-suggestions";
        moroccanCities.forEach(city => {
            const option = document.createElement("option");
            option.value = city;
            datalist.appendChild(option);
        });
        document.body.appendChild(datalist);
        cityField.setAttribute("list", "city-suggestions");

        // auto capitalize first letter
        cityField.addEventListener("input", function () {
            const val = cityField.value;
            if (val.length === 1) {
                cityField.value = val.charAt(0).toUpperCase() + val.slice(1);
            }
        });
    }

});